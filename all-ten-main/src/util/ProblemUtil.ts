/**
 * Creates a Problem object with the problem for a specific day (and dayOffset).
 *
 * @param {number} dayOffset The number of days to offset. A positive number is in the future.
 */
export function createProblemForDate(
	date: Date,
	props?: ProblemProps
): Problem {
	const dayOffset = props?.dayOffset || 0;
	const problemDate = new Date(date);

	problemDate.setDate(problemDate.getDate() + dayOffset);
	const problemNumbers = props?.start
		? props.start
		: getProblemNumbersForDay(problemDate)
				.split("")
				.map((n) => Number(n));

	return createProblem(problemNumbers, props);
}

