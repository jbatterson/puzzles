export function prettifySolution(sol: string): string {
	return sol
		.replace(/\*/g, "\u00d7")
		.replace(/\//g, "\u00f7")
		.replace(/\+/g, "\u002b")
		.replace(/\-/g, "\u2212")
		.split("")
		.join(" ")
		.replace(/ \)/g, "\u0029")
		.replace(/\( /g, "\u0028")
		.replace(/(\d) (?=\d)/g, "$1");
}

export function getProblemNumbersForDay(d: Date): keyof typeof SOLUTIONS {
	const daysSinceLaunch = getDaysSinceLaunch(d);
	return (
		PROBLEMS_BY_DAY[daysSinceLaunch % PROBLEMS_BY_DAY.length] || DEFAULT_PROBLEM
	);
}

function getDaysSinceLaunch(d: Date): number {
	return Math.floor((Number(d) - Number(LAUNCH_DATE)) / DAY_IN_MS);
}

function getPuzzleNumber(d: Date): number {
	return getDaysSinceLaunch(d);
}

/**
 * Gets the puzzle number for the given date, in the form of a string
 * with a minimum of 3 digits (e.g. '009').
 */
const MIN_DIGITS_PUZZLE_NUM = 3;
export function getPuzzleNumberAsString(d: Date): string {
	const puzzleNumber = getPuzzleNumber(d).toString();
	const numDigits = puzzleNumber.length;
	const prefix =
		numDigits < MIN_DIGITS_PUZZLE_NUM
			? "0".repeat(MIN_DIGITS_PUZZLE_NUM - numDigits)
			: "";
	return prefix + puzzleNumber;
}

